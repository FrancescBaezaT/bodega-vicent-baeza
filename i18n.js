/* =========================================================
   i18n ENGINE
   ========================================================= */
const LANG_KEY = 'vb_lang';

function getCurrentLang() {
    return localStorage.getItem(LANG_KEY) || 'es';
}

function setLang(lang) {
    if (!translations[lang]) return;
    localStorage.setItem(LANG_KEY, lang);
    applyTranslations(lang);
    updateLangSelector(lang);
    document.documentElement.lang = lang;
}

function t(key) {
    const lang = getCurrentLang();
    return (translations[lang] && translations[lang][key] !== undefined)
        ? translations[lang][key]
        : (translations['es'][key] || key);
}

function applyTranslations(lang) {
    const T = translations[lang] || translations['es'];
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (T[key] !== undefined) el.innerHTML = T[key];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (T[key] !== undefined) el.placeholder = T[key];
    });
}

function updateLangSelector(lang) {
    const T = translations[lang] || translations['es'];
    const btn = document.getElementById('langSelectorBtn');
    if (btn) btn.innerHTML = `${T.lang_flag} ${T.lang_name} <i class="ri-arrow-down-s-line"></i>`;
    document.querySelectorAll('.lang-option').forEach(opt => {
        opt.classList.toggle('active', opt.dataset.lang === lang);
    });
}

function initLangSelector() {
    const lang = getCurrentLang();
    applyTranslations(lang);
    updateLangSelector(lang);

    const btn = document.getElementById('langSelectorBtn');
    const dropdown = document.getElementById('langDropdown');

    if (btn && dropdown) {
        btn.addEventListener('click', (e) => {
            e.stopPropagation();
            dropdown.classList.toggle('open');
        });
        document.addEventListener('click', () => dropdown.classList.remove('open'));
        dropdown.addEventListener('click', (e) => e.stopPropagation());
        document.querySelectorAll('.lang-option').forEach(opt => {
            opt.addEventListener('click', () => {
                setLang(opt.dataset.lang);
                dropdown.classList.remove('open');
            });
        });
    }
}

document.addEventListener('DOMContentLoaded', initLangSelector);
