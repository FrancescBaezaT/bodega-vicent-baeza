const SUPPORTED_LANGS = ['es', 'ca', 'en', 'fr', 'ru'];
const LANG_KEY = 'vb_lang';
const LOCALES = {
    es: 'es-ES',
    ca: 'ca-ES',
    en: 'en-GB',
    fr: 'fr-FR',
    ru: 'ru-RU'
};
const LANG_UI = {
    es: { code: 'ES', flag: '🇪🇸', flagClass: 'lang-flag-es' },
    ca: { code: 'CA', flag: '', flagClass: 'lang-flag-senyera' },
    en: { code: 'EN', flag: '🇬🇧', flagClass: 'lang-flag-en' },
    fr: { code: 'FR', flag: '🇫🇷', flagClass: 'lang-flag-fr' },
    ru: { code: 'RU', flag: '🇷🇺', flagClass: 'lang-flag-ru' }
};

function readStoredLanguage() {
    try {
        const saved = localStorage.getItem(LANG_KEY);
        return SUPPORTED_LANGS.includes(saved) ? saved : 'es';
    } catch {
        return 'es';
    }
}

let activeLanguage = readStoredLanguage();

function getCurrentLang() {
    return activeLanguage;
}

function interpolate(template, variables = {}) {
    return String(template).replace(/\{(\w+)\}/g, (match, key) => (
        Object.prototype.hasOwnProperty.call(variables, key) ? variables[key] : match
    ));
}

function t(key, variables = {}, lang = getCurrentLang()) {
    const activeLang = SUPPORTED_LANGS.includes(lang) ? lang : 'es';
    const dictionary = translations[activeLang];
    const value = Object.prototype.hasOwnProperty.call(dictionary, key) ? dictionary[key] : key;
    return interpolate(value, variables);
}

function formatCurrency(value, lang = getCurrentLang()) {
    return new Intl.NumberFormat(LOCALES[lang] || LOCALES.es, {
        style: 'currency',
        currency: 'EUR',
        minimumFractionDigits: 2,
        maximumFractionDigits: 2
    }).format(Number(value) || 0);
}

function translateAttribute(selector, attribute, lang) {
    document.querySelectorAll(selector).forEach((element) => {
        const key = element.getAttribute(selector.slice(1, -1));
        if (key) element.setAttribute(attribute, t(key, {}, lang));
    });
}

function applyTranslations(lang = getCurrentLang()) {
    const activeLang = SUPPORTED_LANGS.includes(lang) ? lang : 'es';
    activeLanguage = activeLang;
    document.documentElement.lang = activeLang;
    document.documentElement.dir = 'ltr';

    document.querySelectorAll('[data-i18n]').forEach((element) => {
        element.textContent = t(element.dataset.i18n, {}, activeLang);
    });

    document.querySelectorAll('[data-i18n-html]').forEach((element) => {
        element.innerHTML = t(element.dataset.i18nHtml, {}, activeLang);
    });

    translateAttribute('[data-i18n-placeholder]', 'placeholder', activeLang);
    translateAttribute('[data-i18n-aria-label]', 'aria-label', activeLang);
    translateAttribute('[data-i18n-alt]', 'alt', activeLang);
    translateAttribute('[data-i18n-title]', 'title', activeLang);
    translateAttribute('[data-i18n-content]', 'content', activeLang);

    document.querySelectorAll('[data-i18n-alt-template]').forEach((element) => {
        const number = element.dataset.i18nNumber || '';
        element.alt = t(element.dataset.i18nAltTemplate, { number }, activeLang);
    });

    const page = document.body?.dataset.page;
    if (page) {
        document.title = t(`page_${page}_title`, {}, activeLang);
        const description = document.querySelector('meta[name="description"]');
        if (description) description.content = t(`page_${page}_meta`, {}, activeLang);
    }

    updateLangSelector(activeLang);
    document.documentElement.classList.remove('i18n-pending');
}

function updateLangSelector(lang) {
    const button = document.getElementById('langSelectorBtn');
    const ui = LANG_UI[lang] || LANG_UI.es;
    if (button) {
        const flag = button.querySelector('.lang-current-flag');
        const code = button.querySelector('.lang-current-code');
        if (flag) {
            flag.className = `lang-current-flag lang-flag ${ui.flagClass}`;
            flag.textContent = ui.flag;
        }
        if (code) code.textContent = ui.code;
        button.setAttribute('aria-label', t('language_selector_label', {}, lang));
        button.setAttribute('title', t('language_selector_label', {}, lang));
    }

    document.querySelectorAll('.lang-option').forEach((option) => {
        const active = option.dataset.lang === lang;
        option.classList.toggle('active', active);
        option.setAttribute('aria-selected', String(active));
    });
}

function setLang(lang) {
    if (!SUPPORTED_LANGS.includes(lang)) return;
    activeLanguage = lang;
    try {
        localStorage.setItem(LANG_KEY, lang);
    } catch {
        // The interface still changes even if private browsing blocks persistence.
    }
    applyTranslations(lang);
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

function initLangSelector() {
    const lang = getCurrentLang();
    applyTranslations(lang);

    const button = document.getElementById('langSelectorBtn');
    const dropdown = document.getElementById('langDropdown');
    if (!button || !dropdown) return;
    const selector = button.closest('.lang-selector');
    const options = Array.from(dropdown.querySelectorAll('.lang-option'));

    function closeSelector(restoreFocus = false) {
        dropdown.classList.remove('open');
        dropdown.setAttribute('aria-hidden', 'true');
        dropdown.inert = true;
        button.setAttribute('aria-expanded', 'false');
        if (restoreFocus) button.focus();
    }

    function openSelector(focus = 'active') {
        dropdown.inert = false;
        dropdown.removeAttribute('inert');
        dropdown.classList.add('open');
        dropdown.setAttribute('aria-hidden', 'false');
        button.setAttribute('aria-expanded', 'true');
        const target = focus === 'first'
            ? options[0]
            : focus === 'last'
                ? options[options.length - 1]
                : options.find((option) => option.classList.contains('active')) || options[0];
        window.requestAnimationFrame(() => {
            if (dropdown.classList.contains('open')) target?.focus();
        });
    }

    function moveOptionFocus(current, change) {
        const currentIndex = Math.max(0, options.indexOf(current));
        const nextIndex = (currentIndex + change + options.length) % options.length;
        options[nextIndex]?.focus();
    }

    closeSelector();

    button.addEventListener('click', () => {
        if (dropdown.classList.contains('open')) closeSelector();
        else openSelector();
    });

    button.addEventListener('keydown', (event) => {
        if (event.key === 'ArrowDown' || event.key === 'ArrowUp') {
            event.preventDefault();
            openSelector(event.key === 'ArrowDown' ? 'first' : 'last');
        }
    });

    options.forEach((option) => {
        option.addEventListener('click', () => {
            setLang(option.dataset.lang);
            closeSelector(true);
        });

        option.addEventListener('keydown', (event) => {
            if (event.key === 'ArrowDown' || event.key === 'ArrowUp') {
                event.preventDefault();
                moveOptionFocus(option, event.key === 'ArrowDown' ? 1 : -1);
            } else if (event.key === 'Home' || event.key === 'End') {
                event.preventDefault();
                options[event.key === 'Home' ? 0 : options.length - 1]?.focus();
            } else if (event.key === 'Escape') {
                event.preventDefault();
                event.stopPropagation();
                closeSelector(true);
            }
        });
    });

    document.addEventListener('click', (event) => {
        if (!selector?.contains(event.target)) closeSelector();
    });

    selector?.addEventListener('focusout', () => {
        window.setTimeout(() => {
            if (!selector.contains(document.activeElement)) closeSelector();
        }, 0);
    });

    document.addEventListener('keydown', (event) => {
        if (event.key === 'Escape' && dropdown.classList.contains('open')) {
            event.preventDefault();
            event.stopImmediatePropagation();
            closeSelector(true);
        }
    });
}

